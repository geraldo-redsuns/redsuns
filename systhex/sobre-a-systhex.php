      <h2>Tradição com qualidade        </h2>
      <p>A frente da área cientifica a Systhex   conta, dentre outros, com um dos profissionais mais bem conceituados   da área de implantodontia no Brasil e no exterior Dr. Gastão Valle   Nicolau.  Ele é formado pela Universidade Federal do Paraná,   pós-graduado pela Universidade de Paris e foi o primeiro dentista a   realizar uma cirurgia de implante dentário no Brasil. A publicação   foi em 1987 pela revista APCD.</p>
      <p>Na Systhex, atualmente o Dr. Gastão   ocupa o cargo de Diretor Cientifico e de Desenvolvimento de Novos   Produtos,   contando com uma equipe altamente especializada de engenheiros, que   em conjunto são os responsáveis pela reconhecida qualidade presente   em toda linha de produtos Systhex.
      </p><br />
