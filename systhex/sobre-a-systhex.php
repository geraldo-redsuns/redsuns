      <h2>Systhex tecnologia a seu serviço        </h2>
      <p>Criada a partir de um sonho conjunto   da Dra. Flavia Nicolau Melhem em parceria com seu pai, Dr. Gastão Valle   Nicolau , desde maio de 2003, a Systhex vem consolidando sua importância     no mercado nacional de implantes dentários e hoje é considerada uma   das principais empresas do segmento. </p>
